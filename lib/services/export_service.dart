  /// Export video analytics to CSV
  Future<String> exportVideoAnalyticsToCSV(List<VideoData> videos) async {
    final buffer = StringBuffer();
    
    // CSV Header
    buffer.writeln('Title,Channel,Views,Likes,Comments,Engagement Rate,Published Date,Duration,Category');
    
    // CSV Data
    for (var video in videos) {
      buffer.writeln(
        '"${_escapeCsv(video.title)}",'
        '"${_escapeCsv(video.channelTitle)}",'
        '${video.viewCount},'
        '${video.likeCount},'
        '${video.commentCount},'
        '${video.engagementRate.toStringAsFixed(2)},'
        '${video.publishedAt.toIso8601String()},'
        '${video.duration.inMinutes},'
        '${video.categoryId}',
      );
    }
    
    return _saveFile('video_analytics_${DateTime.now().millisecondsSinceEpoch}.csv', buffer.toString());
  }

